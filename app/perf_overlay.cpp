inline void PutPixel(uint8_t* buffer, int width, int x, int y, uint32_t color) {
    if (x < 0 || x >= width || y < 0 || y >= 1024) {
        return;
    }
    int idx = (y * width + x) * 4;
    buffer[idx + 0] = (color >> 16) & 0xFF;
    buffer[idx + 1] = (color >> 8) & 0xFF;
    buffer[idx + 2] = color & 0xFF;
    buffer[idx + 3] = 0xFF;
}

void DrawChar(uint8_t* buffer, int width, int x, int y, char c, uint32_t color) {
    if (c < 32 || c >= 128) return;
    const uint8_t* bitmap = kFontBitmaps[c - 32];
    for (int dy = 0; dy < kFontHeight; dy++) {
        uint8_t bits = bitmap[dy];
        for (int dx = 0; dx < kFontWidth; dx++) {
            if (bits & (1 << (7 - dx))) {
                PutPixel(buffer, width, x + dx, y + dy, color);
            }
        }
    }
}

void DrawText(uint8_t* buffer, int width, int x, int y, const char* text, uint32_t color) {
    int cx = x;
    while (*text) {
        DrawChar(buffer, width, cx, y, *text, color);
        cx += kFontWidth + 1;
        text++;
    }
}

