void DrawPerformanceOverlay(
    uint8_t* buffer,
    int width,
    int height,
    int num_gaussians,
    const RenderStats& stats,
    double fps) {

    constexpr uint32_t kTextColor = 0xFFFFFFFF;
    constexpr uint32_t kBgColor = 0x80000000;

    int text_x = 5;
    int text_y = 5;

    char line[64];

    snprintf(line, sizeof(line), "FPS: %.1f", fps);
    DrawText(buffer, width, text_x, text_y, line, kTextColor);
    text_y += kFontHeight + 2;

    snprintf(line, sizeof(line), "Gaussians: %d", num_gaussians);
    DrawText(buffer, width, text_x, text_y, line, kTextColor);
    text_y += kFontHeight + 2;

    snprintf(line, sizeof(line), "Total: %.2fms", stats.total_ms);
    DrawText(buffer, width, text_x, text_y, line, kTextColor);
    text_y += kFontHeight + 2;

    snprintf(line, sizeof(line), "SH: %.2fms", stats.sh_eval_ms);
    DrawText(buffer, width, text_x, text_y, line, kTextColor);
    text_y += kFontHeight + 2;

    snprintf(line, sizeof(line), "Project: %.2fms", stats.project_ms);
    DrawText(buffer, width, text_x, text_y, line, kTextColor);
    text_y += kFontHeight + 2;

    snprintf(line, sizeof(line), "Build: %.2fms", stats.build_tile_ms);
    DrawText(buffer, width, text_x, text_y, line, kTextColor);
    text_y += kFontHeight + 2;

    snprintf(line, sizeof(line), "Sort: %.2fms", stats.sort_ms);
    DrawText(buffer, width, text_x, text_y, line, kTextColor);
    text_y += kFontHeight + 2;

    snprintf(line, sizeof(line), "Raster: %.2fms", stats.raster_ms);
    DrawText(buffer, width, text_x, text_y, line, kTextColor);
}

